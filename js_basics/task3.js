// Variables that contain the names of two people
let person1 = "Gin";
let person2 = "Kage";

// Using string concatenation to create a greeting message
let greetingConcatenation = "Good morning, " + person1 + " & " + person2 + "!";

// Output the result to the console
console.log(greetingConcatenation);

// Using template string
const greetingTemplate = `Good morning, ${person1} & ${person2}!`;

// Output the result to the console
console.log(greetingTemplate);